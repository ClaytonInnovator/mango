export function getMetaDataInfo(path: string, locale: string) {
  const allInfo = metadataList.find(e => e.path === path) ?? metadataList[0]
  const langType = locale === 'zhTw' ? 'zhTw' : locale === 'kr' ? 'kr' : 'en'
  const metadataInfo = allInfo[langType]
  const baseUrl = process.env.NEXT_PUBLIC_METADATABASE ?? 'http://localhost:3000'
  return {
    title: metadataInfo.title,
    description: metadataInfo.description,
    keywords: metadataInfo.keysWords,
    metadataBase: new URL(baseUrl),
    openGraph: {
      title: metadataInfo.title,
      description: metadataInfo.description,
      images: '/img/preview1.png'
    },
    twitter: {
      card: "summary_large_image",
      title: metadataInfo.title,
      description: metadataInfo.description,
      images: '/img/preview1.png'
    }
  }
}